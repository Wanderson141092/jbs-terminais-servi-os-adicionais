-- 2) Catálogo de status do motor
CREATE TABLE IF NOT EXISTS public.process_status (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  status_code TEXT NOT NULL UNIQUE,
  status_label TEXT NOT NULL,
  ordem INTEGER NOT NULL,
  ativo BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS public.process_status_transitions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  from_status TEXT NOT NULL,
  to_status TEXT NOT NULL,
  acao TEXT NOT NULL,
  requires_validation BOOLEAN NOT NULL DEFAULT false,
  ativo BOOLEAN NOT NULL DEFAULT true,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  CONSTRAINT process_status_transitions_unique UNIQUE (from_status, to_status, acao)
);

ALTER TABLE public.process_status_transitions
  ADD CONSTRAINT process_status_transitions_from_fk
  FOREIGN KEY (from_status) REFERENCES public.process_status(status_code)
  ON UPDATE CASCADE ON DELETE RESTRICT;

ALTER TABLE public.process_status_transitions
  ADD CONSTRAINT process_status_transitions_to_fk
  FOREIGN KEY (to_status) REFERENCES public.process_status(status_code)
  ON UPDATE CASCADE ON DELETE RESTRICT;

CREATE INDEX IF NOT EXISTS idx_process_status_ativo_ordem
  ON public.process_status(ativo, ordem);

CREATE INDEX IF NOT EXISTS idx_process_status_transitions_lookup
  ON public.process_status_transitions(from_status, to_status, acao)
  WHERE ativo = true;

ALTER TABLE public.process_status ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.process_status_transitions ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Authenticated can view process_status" ON public.process_status;
CREATE POLICY "Authenticated can view process_status"
  ON public.process_status
  FOR SELECT
  USING (auth.uid() IS NOT NULL);

DROP POLICY IF EXISTS "Only admins can manage process_status" ON public.process_status;
CREATE POLICY "Only admins can manage process_status"
  ON public.process_status
  FOR ALL
  USING (has_role(auth.uid(), 'admin'::app_role));

DROP POLICY IF EXISTS "Authenticated can view process_status_transitions" ON public.process_status_transitions;
CREATE POLICY "Authenticated can view process_status_transitions"
  ON public.process_status_transitions
  FOR SELECT
  USING (auth.uid() IS NOT NULL);

DROP POLICY IF EXISTS "Only admins can manage process_status_transitions" ON public.process_status_transitions;
CREATE POLICY "Only admins can manage process_status_transitions"
  ON public.process_status_transitions
  FOR ALL
  USING (has_role(auth.uid(), 'admin'::app_role));

