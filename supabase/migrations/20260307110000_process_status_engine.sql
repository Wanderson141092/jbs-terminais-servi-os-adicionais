-- 5) Engine transacional de mudança de status + auditoria
CREATE OR REPLACE FUNCTION public.process_status_engine(
  p_solicitacao_id UUID,
  p_to_status TEXT,
  p_acao TEXT,
  p_usuario_id UUID DEFAULT auth.uid(),
  p_requires_validation BOOLEAN DEFAULT false,
  p_detalhes TEXT DEFAULT NULL
)
RETURNS public.solicitacoes
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_solicitacao public.solicitacoes%ROWTYPE;
  v_transition public.process_status_transitions%ROWTYPE;
  v_from_status TEXT;
BEGIN
  IF p_usuario_id IS NULL THEN
    RAISE EXCEPTION 'Usuário obrigatório para processar transição de status';
  END IF;

  SELECT *
  INTO v_solicitacao
  FROM public.solicitacoes
  WHERE id = p_solicitacao_id
  FOR UPDATE;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Solicitação % não encontrada', p_solicitacao_id;
  END IF;

  v_from_status := v_solicitacao.status::TEXT;

  SELECT *
  INTO v_transition
  FROM public.process_status_transitions
  WHERE from_status = v_from_status
    AND to_status = p_to_status
    AND acao = p_acao
    AND ativo = true;

  IF NOT FOUND THEN
    RAISE EXCEPTION 'Transição inválida: % --(%)-> %', v_from_status, p_acao, p_to_status;
  END IF;

  IF v_transition.requires_validation AND NOT p_requires_validation THEN
    RAISE EXCEPTION 'Ação % requer validação explícita', p_acao;
  END IF;

  UPDATE public.solicitacoes
  SET
    status = p_to_status::public.status_solicitacao,
    updated_at = now()
  WHERE id = p_solicitacao_id
  RETURNING * INTO v_solicitacao;

  IF to_regclass('public.audit_log') IS NOT NULL
     AND to_regprocedure('public.insert_audit_log(uuid,uuid,text,text,text,uuid)') IS NOT NULL THEN
    PERFORM public.insert_audit_log(
      p_solicitacao_id,
      p_usuario_id,
      'status_transition',
      COALESCE(
        p_detalhes,
        format('status: %s -> %s | acao: %s', v_from_status, p_to_status, p_acao)
      ),
      'solicitacoes',
      p_solicitacao_id
    );
  END IF;

  RETURN v_solicitacao;
END;
$$;

GRANT EXECUTE ON FUNCTION public.process_status_engine(UUID, TEXT, TEXT, UUID, BOOLEAN, TEXT) TO authenticated;
